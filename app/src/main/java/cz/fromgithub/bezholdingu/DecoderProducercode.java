    public ResultData getResultData(Context cont, String producercode) {

        producercode = producercode.replaceFirst ("^0*", "");   // odstrani uvodni nuly
        String nazevFirmy = "Neznámý kód";
        DecoderHelper.Kategorie kategorieFirmy = DecoderHelper.Kategorie.NEJASNE;

        List<FirmaData> producerNamesCZ = getProducerNames();
        nazevFirmy = getProducerNameCZ(producerNamesCZ, producercode);

        if(nazevFirmy.length()>0) {
            if (isFromHolding("cz", producercode)) {
                kategorieFirmy = DecoderHelper.Kategorie.HOLDING;
                nazevFirmy = "AGROFERT - ".concat(nazevFirmy);
            }
            else if (SettingsHelper.getSettingBoolean(cont, SettingsHelper.Preference.TOMAN) && isFromToman("cz", producercode)) {
                kategorieFirmy = DecoderHelper.Kategorie.TOMAN;
                nazevFirmy = nazevFirmy.concat(", Česká republika");
            }
            else {
                kategorieFirmy = DecoderHelper.Kategorie.MIMOHOLDING;
                nazevFirmy = nazevFirmy.concat(", Česká republika");
            }
        }
        else {
            nazevFirmy = "nezjištěná firma z České republiky";
        }

        ResultData rslt = new ResultData();
        rslt.nazev = nazevFirmy;
        rslt.dodatek = "";
        rslt.holding = kategorieFirmy;

        return rslt;
    }

    // najde vyrobce podle produktoveho kodu (cisla)
    private String getProducerNameCZ (List<FirmaData> seznamZnacek, String num) {
        for (FirmaData znacka : seznamZnacek) {
            if (znacka.kod.equals(num))
                return znacka.nazev;
        }
        return "";
    }

    // najde vyrobce podle zacatku nazvu
    public ResultData getProducerNameByName (Context cont, String name) {
        List<FirmaData> producerNamesCZ = getProducerNames();
        for (FirmaData znacka : producerNamesCZ) {
            if (DecoderHelper.normalizeString(znacka.nazev).startsWith(name)) {
                DecoderHelper.Kategorie holding = isFromHolding("cz", znacka.kod) ? DecoderHelper.Kategorie.HOLDING : DecoderHelper.Kategorie.MIMOHOLDING;

                if (isFromToman("cz", znacka.kod))
                    holding = DecoderHelper.Kategorie.TOMAN;

                return new ResultData(znacka.nazev.concat(", Česká republika"), "", holding);
            }
        }
        return null;
    }

    // zjisti, jestli je firma ze zadane zeme a se zadanym kodem z holdingu
